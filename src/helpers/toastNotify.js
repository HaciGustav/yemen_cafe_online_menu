import { toast } from 'react-toastify';

export const toastSuccessNotify = (msg) => {
    toast.success(msg, {
        position: 'top-right',
        autoClose: 1000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: 'light',
    });
};

